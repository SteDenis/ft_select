char	*ft_strcat_path(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	if (i > 0 && s1[i - 1] != '/')
		s1[i++] = '/';
	while (s2[j] && i < 1024)
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
