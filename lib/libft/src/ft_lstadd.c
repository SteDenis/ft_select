#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	if (*alst == NULL)
		*alst = new;
	else
	{
		while (new->next != NULL)
			new = new->next;
		new->next = *alst;
		*alst = new;
	}
}
